    private void jButtonOkRetrieveForThanaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkRetrieveForThanaActionPerformed
        String thana = jTextFieldThana.getText();
        jTextFieldThana.setText("");
        PreparedStatement ps;
        ResultSet rs;
        String query="SELECT * FROM `users` WHERE `duty zone`=?";
        try{
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, thana);
            rs = ps.executeQuery();
            int flag=0;
            while(rs.next()){
                flag=1;
                
                String Name = rs.getString("name");
                String Place = rs.getString("duty zone");
                String Mobile = rs.getString("mobile number");
                
                Name = Name.toUpperCase();
                Place = Place.toUpperCase();
                
                String tbData[] = {Place,Name};
                DefaultTableModel tblModel = (DefaultTableModel)jTableOfficer.getModel();
                
                tblModel.addRow(tbData);
            }
            if(flag==0){
                JOptionPane.showMessageDialog(null,"No record found");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButtonOkRetrieveForThanaActionPerformed

