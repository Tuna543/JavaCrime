    static void mainCaller()throws JSONException{
        BufferedReader reader;
        String line=new String();
        StringBuffer response = new StringBuffer();
        HttpURLConnection connection=null;
        
        try{
            URL u = new URL("https://api.usa.gov/crime/fbi/sapi/api/police-employment/national/1985/2019?api_key=type your API KEY here");
            connection = (HttpURLConnection) u.openConnection();
            
            
            int status = connection.getResponseCode();
            System.out.println("code"+status);
            
            
            if(status>299){
                reader = new BufferedReader(new InputStreamReader(connection.getErrorStream()));
                
                while ((line = reader.readLine()) != null) {
                    response.append(line);
                }
                reader.close();
            }
            else{
                reader = new BufferedReader(new InputStreamReader(connection.getInputStream()));
                
                while ((line = reader.readLine()) != null) {
                    response.append(line);
                }
                reader.close();
            }
            //parse(response.toString());
            //System.out.println(response);
        }/*
        catch(Exception e){
            System.out.println(e);
        }*/
        catch(MalformedURLException e){
            e.printStackTrace();
        }
        catch(IOException e){
            e.printStackTrace();
        }
        finally{
            connection.disconnect();
        }
        
        //JSONPerser jsonparser = new JSONPerser();
        
        //JSONObject response = new JSONObject(line);
        
        //System.out.println(response);
        
        JSONObject myResponse = new JSONObject(response.toString());
        //System.out.println(myResponse);
        JSONArray array= (JSONArray)myResponse.get("results");
        System.out.println("Duty Officers");
        System.out.println(array.length());
        
        for(int i=0; i<size; i++){
            JSONObject results = (JSONObject)array.get(i);
            year[i]= (int)results.get("data_year");
            female[i] = (int)results.get("female_officer_ct");
            male[i] = (int)results.get("male_officer_ct");
            agency[i] = (int)results.get("agency_count_pe_submitting");
        }
    }
